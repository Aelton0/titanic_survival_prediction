import kagglehub
import os

path =kagglehub.dataset_download("yasserh/titanic-dataset")

print("Path to dataset files: ", path)

print(f"Listining data inside path:  {os.listdir(path)}")

path_csv = path + '/Titanic-Dataset.csv'

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
import warnings

df = pd.read_csv(path_csv)
df.set_index("PassengerId", inplace= True)
df.head()

df.shape

df.info()

"""Data Cleaning"""

df.isnull().sum()

df = df.drop(columns={"Cabin","Name","Ticket"})

df.head()

"""Feature Engineering"""

df['Age'] = df["Age"].fillna(df['Age'].mean())
df["Age"] = df["Age"].astype("int64")

df['Age']

df= df.dropna(axis=0)

df.isnull().sum()

df["Sex"] = df["Sex"].map({'male': 0, 'female': 1 })
df['Sex'] = df['Sex'].astype('int64')

df["Embarked"] = df["Embarked"].map({'S': 0, 'C': 1, 'Q': 2 })
df['Embarked'] = df['Embarked'].astype('int64')

sns.pairplot(df)

array_n = np.array([1,2,3])
array_example = np.triu(np.ones_like(array_n))
array_example

numeric_dtypes = df.select_dtypes(include="number")
plt.figure(figsize=(8,7.5))
plt.title("Correlation heatmap")

corr = numeric_dtypes.corr() #correlation

mask = np.triu(np.ones_like(corr,dtype=bool))

sns.heatmap(data=corr,mask=mask,annot=True,fmt='.2f')

n_vars = len(df.columns)
n_cols = 3
n_rows = (n_vars +n_cols - 1) // n_cols

plt.figure(figsize=(8,7.5))

for idx,col in enumerate(df.columns,start=1):
  plt.subplot(n_rows,n_cols,idx)

  sns.histplot(
      df[col],
      kde=True,
      bins=20
  )

  plt.title(f"Distribution plot from {col}")
  plt.xlabel(col)

plt.tight_layout()
plt.show()

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression

x = df.drop(columns="Survived")
y = df["Survived"]

x_train, x_test , y_train , y_test = train_test_split(x,y,stratify=y,train_size=0.8, random_state=42)

print(x_train.shape)
print(x_test.shape)
print(y_train.shape)
print(y_test.shape)

model= LogisticRegression()
model.fit(x_train, y_train)

predict = model.predict(x_test)

from sklearn.metrics import accuracy_score,precision_score,recall_score,f1_score

accuracy = accuracy_score(y_test,predict)
precision = precision_score(y_test,predict)
recall = recall_score(y_test,predict)

print(f"Accuracy {accuracy:.2f}")
print(f"Precision {precision:.2f}")
print(f"Recal {recall:.2f}")

